def zais2zaid(zaid):
    AS2Z = AStoZ()
    AS, A = zaid.split('-')
    A = int(A)
    Z = int(AS2Z[AS])
    if Z < 10:
        Z *= 1000
    else:
        Z *= 10000
        A *= 10

    zaid = Z + A
    return zaid


def zaid2zais(zaid):
    zaid = str(zaid)
    if len(zaid) == 4:
        A = zaid[-3:]
        A_str = A[1:]
    else:
        A = zaid[-4:]
        A_str = A[:-1]
    if A_str[0] == '0':
        A_str = A_str[1:]

    Z = zaid.split(A)[0]
    zais = f'{ZtoAS(Z)}-{A_str}'
    return zais


