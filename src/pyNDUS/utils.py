def np2unp(v, rsd):
    """
    Convert numpy.ndarray into equivalent uncertain numpy.ndarray.

    Parameters
    ----------
    v : numpy.array, list
        Array representing average values.
    rsd : numpy.array, list
        Array representing relative standard deviation values.

    Returns
    -------
    None.

    """
    if v.shape != rsd.shape:
        raise ValueError('Mean and rsd shapes are not consistent!')

    if len(v.shape) == 1:
        # get
        uv = unp.uarray(v, rsd*abs(v))

    return uv


def uncformat(data, std, fmtn=":.2u%S", fmts=None):
    """
    Convert data and std into formatted string for printing.

    Parameters
    ----------
    data : numpy.array, list
        Array of average values.
    std : numpy.array, list
        Array of standard deviations.
    fmt : string
        Format for printing data.
    fmts : string
        Format for standard deviation. Default is None, so format is the same
        of the data.

    Returns
    -------
    out : string
        data+/-std deviation

    """
    if len(data) != len(std):
        raise ValueError("Data and std dimension mismatch!")

    if std is None:
        try:
            # data are ufloat
            inptup = [(d.n, d.s) for d in data]
        except OSError:
            raise TypeError("If std is not provided, data must be ufloat type")
    else:
        inptup = list(zip(data, std))

    fmtn = f'{fmtn}'
    out = []
    outapp = out.append
    for tup in inptup:
        d, s = tup
        outapp(fmtn.format(ufloat(d, s)))
    return out



